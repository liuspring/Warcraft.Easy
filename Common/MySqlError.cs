        public static String GetErrDesc(Int32 code)
        {
            if (err_desc_ == null)
            {
                lock (desc_locker_)
                {
                    if (err_desc_ == null)
                    {
                        err_desc_ = new Dictionary<Int32, String>();
                        InitErrDesc();
                    }
                }
            }
            if (err_desc_.ContainsKey(code))
                return String.Format("{0} : {1}", code, err_desc_[code]);
            else
                return String.Format("unknown error : {0}", code);
        }

