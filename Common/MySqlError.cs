        public static Int32 TransError(Int32 mysql_err)
        {
            if (err_trans_ == null)
            {
                lock (trans_lock_)
                {
                    if (err_trans_ == null)
                    {
                        err_trans_ = new Dictionary<Int32, Int32>();
                        InitErrTrans();
                    }
                }
            }
            if (err_trans_.ContainsKey(mysql_err))
                return err_trans_[mysql_err];
            else
                return Error.DB_ERR_UNKNOWN;
        }
